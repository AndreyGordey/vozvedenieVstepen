//  Задание: Дополните функцию квадратной суммы так, 
// чтобы она возводила в квадрат каждое переданное ей число, 
// а затем суммировала результаты.

//В этой функции мы используем метод reduce для итераций по массиву чисел и 
// возводим каждое число в квадрат с помощью Math.pow(num, 2). 
// Наконец, мы суммируем все возведенные в квадрат числа и возвращаем результат.

function squareSum(numbers) {
    return numbers.reduce((sum, num) => sum + Math.pow(num, 2), 0);
}

// Example usage
const numbers = [1, 2, 3, 4];
console.log(squareSum(numbers)); // Output: 30
